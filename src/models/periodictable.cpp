PeriodicTable::PeriodicTable()
{
    for(uint16_t z = 1; z <= N_ELEMENTS; z++)
    {
        element x;
        x.z = z;
        x.name = element_names[z];
        addIsotopesTo(x);
        table.push_back(x);
    }
}

void PeriodicTable::addIsotopesTo(element& x)
{
    auto isotopesOfX = element_isotopes[x.z - 1];
    for(uint16_t i = 0; i < element_isotopes_sizes[x.z - 1]; i++)
    {
        x.isotopes.push_back(isotopesOfX[i][0]);
        x.half_lives.push_back(isotopesOfX[i][1]);
    }
    printf("%s\n", x.name.c_str());
    return;
}

int16_t PeriodicTable::fromAtomicMassToIsotopeIndex(uint16_t z, uint16_t a)
{
    uint16_t len = element_isotopes_sizes[z];
    for(uint16_t i = 0; i < len; i++)
    {
        if(table[z].isotopes.at(i) == a)
        {
            return i;
        }
    }

    return -1;
}

bool PeriodicTable::isotopeExists(uint16_t z, uint16_t a)
{
    return fromAtomicMassToIsotopeIndex(z, a) != -1;
}
